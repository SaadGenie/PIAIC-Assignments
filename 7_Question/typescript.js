"use strict";
// As the Result of each operation will be fixed so i am using const data type
// Storing 5+3 result in the constant variable addition of number datatype
const addition = 5 + 3;
// Displaying the value of variable on the user screen
console.log("Addition Result: " + addition);
// Storing 10 - 2 result in the constant variable subtraction of number datatype
const subtraction = 10 - 2;
// Displaying the value of variable on the user screen
console.log("Subtraction Result: " + subtraction);
// Storing 4 * 2 result in the constant variable multiplication of number datatype
const multiplication = 4 * 2;
// Displaying the value of variable on the user screen
console.log("Multiplication Result: " + multiplication);
// Storing 16 / 2 result in the constant variable Division of number datatype
const Division = 16 / 2;
// Displaying the value of variable on the user screen
console.log("Division  Result: " + Division);
